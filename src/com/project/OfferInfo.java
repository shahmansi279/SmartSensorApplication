package com.project;

public class OfferInfo {

	private String offerId;
	private String offerTitle;
	private String offerDesc;
	private String offerPlaceId;
	private String offerIconUrl;
	private String offerValidity;

	public String getOfferValidity() {
		return offerValidity;
	}

	public void setOfferValidity(String offerValidity) {
		this.offerValidity = offerValidity;
	}

	public String getOfferId() {
		return offerId;
	}

	public void setOfferId(String offerId) {
		this.offerId = offerId;
	}

	public String getOfferTitle() {
		return offerTitle;
	}

	public void setOfferTitle(String offerTitle) {
		this.offerTitle = offerTitle;
	}

	public String getOfferDesc() {
		return offerDesc;
	}

	public void setOfferDesc(String offerDesc) {
		this.offerDesc = offerDesc;
	}

	public String getOfferPlaceId() {
		return offerPlaceId;
	}

	public void setOfferPlaceId(String offerPlaceId) {
		this.offerPlaceId = offerPlaceId;
	}

	public String getOfferIconUrl() {
		return offerIconUrl;
	}

	public void setOfferIconUrl(String offerIconUrl) {
		this.offerIconUrl = offerIconUrl;
	}

}
