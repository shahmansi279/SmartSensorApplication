package com.project;

public class EventInfo {

	protected String eventTitle;
	protected String eventPlaceId;
	protected String eventId;
	protected String eventTiming;
	protected String eventAddr;
	protected String eventIconUrl;
	protected String eventDesc;

	protected double eLat;
	protected double eLong;
	public String getEventTitle() {
		return eventTitle;
	}
	public void setEventTitle(String eventTitle) {
		this.eventTitle = eventTitle;
	}
	public String getEventPlaceId() {
		return eventPlaceId;
	}
	public void setEventPlaceId(String eventPlaceId) {
		this.eventPlaceId = eventPlaceId;
	}
	public String getEventId() {
		return eventId;
	}
	public void setEventId(String eventId) {
		this.eventId = eventId;
	}
	public String getEventTiming() {
		return eventTiming;
	}
	public void setEventTiming(String eventTiming) {
		this.eventTiming = eventTiming;
	}
	public String getEventAddr() {
		return eventAddr;
	}
	public void setEventAddr(String eventAddr) {
		this.eventAddr = eventAddr;
	}
	public String getEventIconUrl() {
		return eventIconUrl;
	}
	public void setEventIconUrl(String eventIconUrl) {
		this.eventIconUrl = eventIconUrl;
	}
	public String getEventDesc() {
		return eventDesc;
	}
	public void setEventDesc(String eventDesc) {
		this.eventDesc = eventDesc;
	}
	public double geteLat() {
		return eLat;
	}
	public void seteLat(double eLat) {
		this.eLat = eLat;
	}
	public double geteLong() {
		return eLong;
	}
	public void seteLong(double eLong) {
		this.eLong = eLong;
	}
	

}
